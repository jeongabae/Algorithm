package com.study.baekjun.while_4;

import java.util.Scanner;

public class Bj10951 {
    public static void main(String[]args){
        Scanner sc = new Scanner(System.in);


        while (sc.hasNext()){
            int A = sc.nextInt();
            int B = sc.nextInt();
            System.out.println(A+B);

        }
    }
}
